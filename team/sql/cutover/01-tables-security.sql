create table if not exists public.users_directory (
  id bigint generated by default as identity primary key,
  department text not null,
  employee_name text not null,
  access_code_hash text not null,
  email text default '',
  changed_at timestamptz not null default now(),
  changed_by text default '',
  source text default 'migration',
  active boolean not null default true,
  unique (department, employee_name)
);

create index if not exists idx_users_directory_lookup
  on public.users_directory (department, employee_name, active);

create table if not exists public.admins_directory (
  id bigint generated by default as identity primary key,
  admin_name text not null unique,
  admin_code_hash text not null,
  role text not null default 'Admin',
  allowed_departments_json jsonb not null default '["All"]'::jsonb,
  changed_at timestamptz not null default now(),
  changed_by text default '',
  source text default 'migration',
  active boolean not null default true
);

create index if not exists idx_admins_directory_lookup
  on public.admins_directory (admin_name, active);

create table if not exists public.task_submissions (
  id bigint generated by default as identity primary key,
  request_id text unique,
  stage text not null check (stage in ('SOD', 'EOD')),
  submitted_at timestamptz not null default now(),
  work_date date not null,
  department text not null,
  employee_name text not null,
  access_code text,
  task_count integer not null default 0,
  total_spent_minutes integer not null default 0,
  payload_json jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists idx_task_submissions_lookup
  on public.task_submissions (department, employee_name, stage, work_date, submitted_at);

create index if not exists idx_task_submissions_work_date
  on public.task_submissions (work_date);

create index if not exists idx_task_submissions_stage_work_date
  on public.task_submissions (stage, work_date, submitted_at desc);

create index if not exists idx_task_submissions_canonical_lookup
  on public.task_submissions (
    stage,
    regexp_replace(lower(coalesce(department, '')), '[^a-z]', '', 'g'),
    lower(trim(employee_name)),
    work_date,
    submitted_at desc
  );

create index if not exists idx_task_submissions_dashboard_lookup
  on public.task_submissions (
    stage,
    work_date,
    public.canonical_department_key(department),
    lower(trim(employee_name)),
    submitted_at desc
  );

