assignment_latest as (
  select distinct on (coalesce(nullif(e.task_id, ''), nullif(e.payload_json->>'taskId', ''), lower(trim(coalesce(e.title, '')))))
    coalesce(nullif(e.task_id, ''), nullif(e.payload_json->>'taskId', ''), '') as task_id,
