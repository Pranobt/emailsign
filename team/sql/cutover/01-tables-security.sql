create table if not exists public.task_admin_events (
  id bigint generated by default as identity primary key,
  event_key text unique,
  event_type text not null check (event_type in ('assignment', 'recurring', 'planner_task', 'planner_consumed', 'cliq_webhook', 'carryover_removed', 'carryover_renamed', 'attendance_override')),
  changed_at timestamptz not null default now(),
  work_date date,
  department text not null,
  employee_name text not null,
  task_id text,
  title text,
  status text,
  payload_json jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

do $$
begin
  if exists (
    select 1
    from pg_constraint c
    where c.conname = 'task_admin_events_event_type_check'
      and c.conrelid = 'public.task_admin_events'::regclass
  ) then
    alter table public.task_admin_events drop constraint task_admin_events_event_type_check;
  end if;
  alter table public.task_admin_events
    add constraint task_admin_events_event_type_check
    check (event_type in ('assignment', 'recurring', 'planner_task', 'planner_consumed', 'cliq_webhook', 'carryover_removed', 'carryover_renamed', 'attendance_override'));
exception
  when duplicate_object then null;
end $$;

