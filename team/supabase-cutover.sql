create table if not exists public.users_directory (
  id bigint generated by default as identity primary key,
  department text not null,
  employee_name text not null,
  access_code_hash text not null,
  email text default '',
  changed_at timestamptz not null default now(),
  changed_by text default '',
  source text default 'migration',
  active boolean not null default true,
  unique (department, employee_name)
);

create index if not exists idx_users_directory_lookup
  on public.users_directory (department, employee_name, active);

create table if not exists public.admins_directory (
  id bigint generated by default as identity primary key,
  admin_name text not null unique,
  admin_code_hash text not null,
  role text not null default 'Admin',
  allowed_departments_json jsonb not null default '["All"]'::jsonb,
  changed_at timestamptz not null default now(),
  changed_by text default '',
